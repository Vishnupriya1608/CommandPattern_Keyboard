package Comman_pattern;

public class GamingKeyboardCommands {
	public void GamingKeyboard_TurnOn() {
		System.out.println("Turn on the Gaming computer ");
	}

	public void Turnoff() {
		System.out.println("Shutdown the Gaming computer ");
	}
	public void GamingSetVolume(int Volume) {
		System.out.println("Default Volume for Gaming Computer is set to: "+Volume);
	}
	public void GamingSetBrightness(int Brightness) {
		System.out.println("Default Brightness for Gaming Computer is set to: "+Brightness);
	}
	public void GamingF12()
	{
		System.out.println("Game mode key is on");
	}

	

}
